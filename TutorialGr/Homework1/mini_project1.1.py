import math
#Δεν χρησιμοποιήθηκαν έλεγχοι για μηδενικές τιμές. Θεωρούμε ότι ο χρήστης δίνει μη μηδενική τιμή.
x=(int(input('Δώστε έναν ακέραιο αριθμό εκτός από 0: ')))
y=(int(input('Δώστε έναν ακέραιο αριθμό εκτός από 0: ')))

k=x/y
b=x%y
j=math.log(abs(x),abs(y))
d=math.sqrt(abs(x))
e=math.exp(x)

print('{:10.5f}{:10d}{:10.5f}{:10.5f}{:10.5f}'.format(k,b,j,d,e))